// ********************************************************************************************************************
import { ITerrainVariance } from './terrain-variance.interface';
// ********************************************************************************************************************
export class TerrainVariance implements ITerrainVariance {

    // ****************************************************************************************************************
    // varianceNe - the variance north east
    // ****************************************************************************************************************
    public varianceNe: ITerrainVariance | null = null;

    // ****************************************************************************************************************
    // varianceNw - the variance north west
    // ****************************************************************************************************************
    public varianceNw: ITerrainVariance | null = null;

    // ****************************************************************************************************************
    // varianceSe - the variance south east
    // ****************************************************************************************************************
    public varianceSe: ITerrainVariance | null = null;

    // ****************************************************************************************************************
    // varianceSw - the variance south west
    // ****************************************************************************************************************
    public varianceSw: ITerrainVariance | null = null;

    // ****************************************************************************************************************
    // constructor
    // ****************************************************************************************************************
    constructor(public readonly variance: number) { }
}
