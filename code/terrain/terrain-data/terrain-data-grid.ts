import { Array2 } from "../../types/array2";
import { TerrainData } from "./terrain-data";
// ********************************************************************************************************************
export class TerrainDataGrid extends Array2<TerrainData> {

    // ****************************************************************************************************************
    // constructor
    // ****************************************************************************************************************
    constructor(sizeX: number, sizeY: number) { super(sizeX, sizeY, true); }

    // ****************************************************************************************************************
    // function:    createDefault
    // ****************************************************************************************************************
    // parameters:  n/a
    // ****************************************************************************************************************
    // returns:     the default element
    // ****************************************************************************************************************
    protected createDefault(): TerrainData {

        return new TerrainData(0);
    }
}
