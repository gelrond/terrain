// ********************************************************************************************************************
import { Array2 } from "../../types/array2";
import { TerrainData } from "./terrain-data";
// ********************************************************************************************************************
export class TerrainDataGrid extends Array2<TerrainData> {

    // ****************************************************************************************************************
    // constructor
    // ****************************************************************************************************************
    constructor(sizeX: number, sizeY: number) {

        super(sizeX, sizeY);

        for (var x = 0; x < this.sizeX; x++) {

            for (var y = 0; y < this.sizeY; y++) {

                this.array[x][y] = new TerrainData(0);
            }
        }
    }
}
